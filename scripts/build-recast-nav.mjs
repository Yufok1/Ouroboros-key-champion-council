import * as esbuild from 'esbuild';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');

const entryPoint = path.join(__dirname, 'recast-nav-browser-entry.mjs');
const outfile = path.join(root, 'static', 'recast-nav.bundle.js');

await esbuild.build({
  entryPoints: [entryPoint],
  outfile,
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: ['es2020'],
  minify: false,
  sourcemap: false,
  logLevel: 'info',
  globalName: 'RecastNavBundle',
  banner: {
    js: '/* generated by scripts/build-recast-nav.mjs */',
  },
});
